#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (i < n && s1[i] != '\0')
		i++;
	while (j < n && s2[j] != '\0')
		j++;
	return (i - j);
}
